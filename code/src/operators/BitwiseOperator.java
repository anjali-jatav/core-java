package operators;

public class BitwiseOperator {

	public static void main(String[] args) {
		System.out.println(2 & 4); //compare the bits of both no's. 
		System.out.println(2 | 3); //compare the bits of both no's. 
		System.out.println(4 ^ 5); //compare the bits of both no's.
		int a=-8;  //bitwise compliment/ ~ tild operator 
		System.out.println(~a);

	}

}
